    public function findEmail($getString){

        $getDomain = substr($getString, strpos($getString, "@") + 1);

        if(empty($getDomain)) {
            return false;
        }
        $getEmails = $this->EmailList();
        $checkStatus = false;
        foreach($getEmails as $singleEmail) {
            $getStatus = stripos($getDomain, $singleEmail);
            if($getStatus !== false){
                $checkStatus = array(
                    true,
                    $singleEmail,
                    strlen($singleEmail)
                );
                break;
            }
        }
        if($checkStatus != false) {
            return $checkStatus;
        }
        return false;
    }

